package CalculadoraSimples;

public class Operacoes {
    public static double somaValor(double numero1, double numero2){
        return (numero1+numero2);
        
    } 
    public static double subtracaoValor(double numero1, double numero2){
        return (numero1-numero2);
        
    }
    public static double multiplicacaoValor(double numero1, double numero2){
        return (numero1*numero2);
        
    }
    public static double divisaoValor(double numero1, double numero2){
        return (numero1/numero2);  
    }
}
